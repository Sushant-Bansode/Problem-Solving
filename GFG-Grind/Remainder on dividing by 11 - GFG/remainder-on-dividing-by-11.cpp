class Solution
{

public:
    int xmod11(string x)
    {
       // code here
       int n = x.length( );
       int temp = 0;
       for(int i = 0; i < n; i++){
        int dig = x[i]-'0';
         temp *= 10;
          temp += dig;
        temp %= 11;   
       }
    return temp%11;
    }
};


